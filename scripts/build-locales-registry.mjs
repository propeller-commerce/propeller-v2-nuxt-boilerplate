#!/usr/bin/env node
// Scan app/locales/<lang>/*.json and write app/locales/_registry.ts.
// English ('en') is canonical. Missing namespaces in other languages emit a
// warning and are written as {} placeholders so the build stays green.
//
// Path differs from propeller-vue (app/locales/) — Nuxt's srcDir is app/, so
// the dictionaries land under app/locales/ instead. The shape of the
// emitted _registry.ts is identical, so the file-provider can be ported
// verbatim.

import { readdirSync, writeFileSync, statSync } from 'node:fs';
import { join, basename } from 'node:path';

const LOCALES_DIR = join(process.cwd(), 'app', 'locales');

function listLangs() {
  return readdirSync(LOCALES_DIR)
    .filter((f) => {
      const p = join(LOCALES_DIR, f);
      return statSync(p).isDirectory();
    })
    .sort();
}

function listNamespaces(lang) {
  const dir = join(LOCALES_DIR, lang);
  return readdirSync(dir)
    .filter((f) => f.endsWith('.json'))
    .map((f) => basename(f, '.json'))
    .sort();
}

const langs = listLangs();
if (langs.length === 0) {
  console.error('No language directories found under app/locales/');
  process.exit(1);
}
if (!langs.includes('en')) {
  console.error('app/locales/en/ is required as the canonical namespace set');
  process.exit(1);
}

const canonicalNamespaces = listNamespaces('en');
const warnings = [];

for (const lang of langs) {
  if (lang === 'en') continue;
  const present = new Set(listNamespaces(lang));
  for (const ns of canonicalNamespaces) {
    if (!present.has(ns)) warnings.push(`Missing app/locales/${lang}/${ns}.json (will be {} at runtime)`);
  }
  for (const ns of present) {
    if (!canonicalNamespaces.includes(ns)) warnings.push(`Extra app/locales/${lang}/${ns}.json (no matching app/locales/en/${ns}.json)`);
  }
}

const lines = [];
lines.push('// AUTO-GENERATED by scripts/build-locales-registry.mjs');
lines.push('// DO NOT EDIT — run `npm run locales:build` instead.');
lines.push('');

for (const lang of langs) {
  const present = listNamespaces(lang);
  for (const ns of present) {
    if (lang !== 'en' && !canonicalNamespaces.includes(ns)) continue;
    const id = `${lang}${ns}`;
    lines.push(`import ${id} from './${lang}/${ns}.json';`);
  }
}
lines.push('');

lines.push('export const registry = {');
for (const lang of langs) {
  lines.push(`  ${lang}: {`);
  for (const ns of canonicalNamespaces) {
    const present = listNamespaces(lang);
    if (present.includes(ns)) {
      lines.push(`    ${ns}: ${lang}${ns},`);
    } else {
      lines.push(`    ${ns}: {} as Record<string, string>,`);
    }
  }
  lines.push('  },');
}
lines.push('} as const;');
lines.push('');
lines.push("export type RegistryLocale = keyof typeof registry;");
lines.push("export type RegistryNamespace = keyof typeof registry['en'];");
lines.push('');

writeFileSync(join(LOCALES_DIR, '_registry.ts'), lines.join('\n'), 'utf8');
console.log(`Wrote app/locales/_registry.ts (${langs.length} languages, ${canonicalNamespaces.length} namespaces)`);
if (warnings.length > 0) {
  console.warn('\nWarnings:');
  for (const w of warnings) console.warn(`  - ${w}`);
}
